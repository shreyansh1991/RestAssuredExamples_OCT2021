package pojo;

public class SimplePojo {
    private String key1;
    private String key2;

    public SimplePojo setKey1(String key1) {
        this.key1 = key1;
        return this;
    }

    public SimplePojo setKey2(String key2) {
        this.key2 = key2;
        return this;
    }

    public String getKey1() {
        return key1;
    }

    public String getKey2() {
        return key2;
    }
}
